
    function change()
    {
        document.getElementById("reds").style.color="rgba(116, 29, 29, 1)";
        document.getElementById("blacks").style.color="rgb(0.16, 0.16, 0.16)";
        document.getElementById("blacks").style.fontSize="15px";
        document.getElementById("reds").style.fontSize="15px";

    }
    function unchange()
    {
        document.getElementById("reds").style.color="white";
        document.getElementById("blacks").style.color="white";
        document.getElementById("blacks").style.fontSize="14px";


    }

